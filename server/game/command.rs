//! Командная шина sim-loop (фаза P1, см. `docs/ECS_REWRITE.md`).
//!
//! Целевая модель: per-connection таски — чистый I/O (декод wire → `SessionCommand`
//! → пуш в шину), а применение мутаций сериализуется через единственного
//! consumer'а, а не размазано по conn-таскам под общим `RwLock`.
//!
//! Фаза P1: труба установлена, один event (TY `TADG`) проходит через шину
//! end-to-end. Остальные события пока на старом пути; переводятся в P2.
//! Применение в P1 всё ещё идёт через существующие хендлеры (они сами лочат
//! ECS) — перенос владения миром в sim это P2.

/// Игровое намерение игрока, декодированное из wire и поставленное в шину.
/// Применяется авторитетно consumer'ом (см. `net::lifecycle::spawn_command_consumer`).
#[derive(Debug, Clone)]
pub enum SessionCommand {
    /// TY `TADG` — переключить авто-копание.
    ToggleAutoDig,
}
