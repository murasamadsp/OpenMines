class Anim {
	/** @type {Set<Anim>} */
	static FinishedAnims = new Set();

	/**
	 * @param {Number} animID 
	 * @param {Vector2d} position 
	 * @param {Number} rotation 
	 */
	constructor(animID, position, rotation) {
		this.Init(animID, position, rotation);
	}

	/**
	 * @param {Number} animID 
	 * @param {Vector2d} position 
	 * @param {Number} rotation 
	 */
	static GetExemplar(animID, position, rotation) {
		if (Anim.FinishedAnims.size) {
			let firstAnim = Anim.FinishedAnims.values().next().value;
			Anim.FinishedAnims.delete(firstAnim);
			firstAnim.Init(animID, position, rotation);
			return firstAnim;
		}
		else {
			return new Anim(animID, position, rotation);
		}
	}

	/**
	 * @param {Number} animID 
	 * @param {Vector2d} position 
	 * @param {Number} rotation 
	 */
	Init(animID, position, rotation) {
		this.id = animID;
		this.position ? this.position.CopyV2(position) : this.position = new Vector2d().CopyV2(position);
		this.rotation = rotation;
		this.timestamp = performance.now();
		this.isFinished = false;
	}

	get IsEnded() {
		if (this.isFinished) {
			return true;
		}
		else {
			Anim.FinishedAnims.add(this);
			this.isFinished = (Math.floor((performance.now() - this.timestamp) * Animations[this.id].speed / 1000)) >= (Animations[this.id].frames.length);
			return this.isFinished;
		}
	}

	GetCurrentFrame() {
		let curframe = Math.floor((performance.now() - this.timestamp) * Animations[this.id].speed / 1000);
		if (curframe < Animations[this.id].frames.length) {
			return { frame: Animations[this.id].frames[curframe], src: Animations[this.id].sourceImage };
		}
		return null;
	}
}

class DropAnim {
	/** @type {Set<DropAnim>} */
	static FinishedAnims = new Set();

	/**
	 * @param {Number} x 
	 * @param {Number} y 
	 * @param {Vector2d} botVisPosition 
	 * @param {"v"|"c"|"w"|"b"|"g"|"r"} cryType 
	 * @param {Number} value
	 * @returns {DropAnim}
	 */
	static GetExemplar(x, y, botVisPosition, cryType, value) {
		if (DropAnim.FinishedAnims.size) {
			let firstAnim = DropAnim.FinishedAnims.values().next().value;
			DropAnim.FinishedAnims.delete(firstAnim);
			firstAnim.Init(x, y, botVisPosition, cryType, value);
			return firstAnim;
		}
		else {
			return new DropAnim(x, y, botVisPosition, cryType, value);
		}
	}

	static CryColors = {
		"b": "#66e",
		"c": "#0ff",
		"g": "#0e0",
		"r": "#e00",
		"v": "#e0e",
		"w": "#fff"
	}
	/**
	 * @param {Number} x 
	 * @param {Number} y 
	 * @param {Vector2d} botVisPosition 
	 * @param {"v"|"c"|"w"|"b"|"g"|"r"} cryType 
	 * @param {Number} value 
	 */
	constructor(x, y, botVisPosition, cryType, value) {
		this.Init(x, y, botVisPosition, cryType, value);
	}

	/**
	 * @param {Number} x 
	 * @param {Number} y 
	 * @param {Vector2d} botVisPosition 
	 * @param {"v"|"c"|"w"|"b"|"g"|"r"} cryType 
	 * @param {Number} value 
	 */
	Init(x, y, botVisPosition, cryType, value) {
		this.position ? this.position.Set(x, y) : (this.position = new Vector2d(x, y));
		this.text = String(`+${value}`);
		this.firstGoTo ? this.firstGoTo.Set(x, y - 1) : (this.firstGoTo = new Vector2d(x, y - 1));
		this.firstTarget = true;
		this.endPos = botVisPosition;
		this.color = DropAnim.CryColors[cryType];
		this.isFinished = false;
	}

	Update() {
		if (this.firstTarget) {
			this.position.Lerp2d(this.firstGoTo, 0.18);
			if (this.position.Distance(this.firstGoTo) < 0.1) {
				this.firstTarget = false;
			}
		}
		else {
			let dist = this.position.Distance(this.endPos);
			if (dist < 0.1 || dist > 3) {
				this.isFinished = true;
				DropAnim.FinishedAnims.add(this);
			}
			this.position.Lerp2d(this.endPos, 0.66);
		}

	}
}

